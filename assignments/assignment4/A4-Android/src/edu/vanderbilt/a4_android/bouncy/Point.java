package edu.vanderbilt.a4_android.bouncy;

/** 
 * @class Point
 * 
 * @brief A point on a Cartesian plane 
 */
public class Point {
    /**
     * The X and Y coordinates of the Point.
     */
    public int x, y;
	
    /**
     * Constructor initializes the data members.
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
